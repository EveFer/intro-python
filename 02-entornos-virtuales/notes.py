# El flujo de trabajo basico se ve asi:

# 1. Crear un entorno virtual que no afecte al resto de la máquina.
# 2. Ingresar al entorno virtual, donde especifica la versión de Python y las bibliotecas que necesita.
# 3. Desarrolla tu programa.

# Crear un entorno
# python3 -m venv <name_env>

# Activar entorno
# source <name_env>/bin/activate

# Para ver paquetes instalados en el entorno
# pip3 freeze

# Desactivar un entorno

# deactivate
